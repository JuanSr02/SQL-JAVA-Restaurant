    private void EliminarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarBActionPerformed
      int x=0,y=0; ResultSet rs;
        if(ElTF.getText().trim().equals("")){
         JOptionPane.showMessageDialog(null, "No se ha ingresado ningun codigo", "Error al eliminar (Campos vacios)", JOptionPane.ERROR_MESSAGE);
         return;
      }
        try{
            y=Integer.parseInt(ElTF.getText());
        }catch(NumberFormatException e){
          JOptionPane.showMessageDialog(null, "Se ha ingresado un codigo invalido", "Error al eliminar (Codigo Invalido)", JOptionPane.ERROR_MESSAGE);
            return;}
          try {
              x=Queries.EliminarMozo(y);
              if(x==0){
                    JOptionPane.showMessageDialog(null, "No se encontró un mozo con el codigo solicitado", "Error al eliminar (Mozo inexistente con el codigo ingresado)", JOptionPane.ERROR_MESSAGE);
              }else if (x==1){
                    JOptionPane.showMessageDialog(null, "Mozo eliminado exitosamente", "Borrado exitoso", JOptionPane.INFORMATION_MESSAGE);
                    ElTF.setText("");
                    rs = Queries.MostrarMozos();
                    Tabla1.setModel(resultToTable(rs));
              }
          } catch (SQLException ex) {
            if ("23503".equals(ex.getSQLState())){
                JOptionPane.showMessageDialog(null, "El Mozo que quiere remover se encuentra asignado a una mesa\nReintente cuando el Mozo se encuentre desocupado", "No se pudo eliminar (Mozo Ocupado)", JOptionPane.ERROR_MESSAGE);
            } else{
                MainFrame.ErrorConexion();
            }
          }
    }//GEN-LAST:event_EliminarBActionPerformed

