    private void InsertarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarBActionPerformed
        ResultSet rs; int x=0;
        if(Codigo.getText().trim().equals("") || Nombre.getText().trim().equals("") || Descripcion.getText().trim().equals("") || PrecioC.getText().trim().equals("") || PrecioV.getText().trim().equals("") || PrecioP.getText().trim().equals("")){
           JOptionPane.showMessageDialog(null,"Hay campos vacios", "Error al insertar (Campos Vacios)", JOptionPane.ERROR_MESSAGE);
           return;
        }
        else{
            try{
            if (Integer.parseInt(Codigo.getText()) < 100 || Integer.parseInt(Codigo.getText()) > 9999 || Integer.parseInt(Codigo.getText())%10!=0){
               JOptionPane.showMessageDialog(null, "Hubo un error en el formato del codigo", "Error al insertar (Codigo Invalido)", JOptionPane.ERROR_MESSAGE);
                return;}
            } catch (NumberFormatException e){
               JOptionPane.showMessageDialog(null, "Hubo un error en el formato del codigo", "Error al insertar (Codigo Invalido)", JOptionPane.ERROR_MESSAGE);
               return;
            }
            try{
            if (Integer.parseInt(PrecioC.getText()) > Integer.parseInt(PrecioV.getText()) || Integer.parseInt(PrecioP.getText()) > Integer.parseInt(PrecioV.getText())){
                JOptionPane.showMessageDialog(null, "Hay Valores invalidos en los Precios (Precio Costo debe ser el menor y Venta el mayor)", "Error al insertar (Precios Invalidos)", JOptionPane.ERROR_MESSAGE);
                return;}
            } catch (NumberFormatException e){
               JOptionPane.showMessageDialog(null, "Hubo un error en el formato de los Precios", "Error al insertar (Precios Invalidos)", JOptionPane.ERROR_MESSAGE);
               return;
            }
            try {
                x=Queries.InsertarPlato(Integer.parseInt(Codigo.getText()), Nombre.getText(), Descripcion.getText(), Tipo.getSelectedItem().toString() , Integer.parseInt(PrecioC.getText()),Integer.parseInt(PrecioV.getText()),Integer.parseInt(PrecioP.getText()));
             }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ya hay un plato con el codigo ingresado", "Error al insertar (Ya existe un plato con el codigo Ingresado)", JOptionPane.ERROR_MESSAGE);
                 return;
             }
                        JOptionPane.showMessageDialog(null, "Plato Insertado Correctamente!","Plato insertado",JOptionPane.INFORMATION_MESSAGE);
                        this.InsertarPlato.setVisible(false);
                        this.Mostrar.setVisible(true);
                        Codigo.setText("");
                        Nombre.setText("");
                        Descripcion.setText("");
                        PrecioC.setText("");
                        PrecioV.setText("");
                        PrecioP.setText("");
                        try {
                            rs = Queries.MostrarPlatos();
                            Tabla.setModel(resultToTable(rs));
                        } catch (SQLException ex) {
                        MainFrame.ErrorConexion();
                        }
                            }
                
    }//GEN-LAST:event_InsertarBActionPerformed

